"""
1. Assign (=)  
   - Assigns the value on the right to the variable on the left.
   - Example: x = 5 (Now x holds the value 5).

2. Add and assign (+=)  
   - Adds the value on the right to the variable on the left and then assigns the result to the variable.
   - Example: x += 3 is equivalent to x = x + 3.

3. Subtract and assign (-=)  
   - Subtracts the value on the right from the variable on the left and then assigns the result to the variable.
   - Example: x -= 2 is equivalent to x = x - 2.

4. Multiply and assign (*=)  
   - Multiplies the variable by the value on the right and then assigns the result to the variable.
   - Example: x *= 4 is equivalent to x = x * 4.

5. Divide and assign (/=)  
   - Divides the variable by the value on the right and then assigns the result to the variable (as a float).
   - Example: x /= 2 is equivalent to x = x / 2.

6. Modulus and assign (%=)  
   - Takes the modulus and assigns the result to the variable.
   - Example: x %= 5 is equivalent to x = x % 5.

7. Exponentiation and assign (**=)  
   - Raises the variable to the power of the value on the right and assigns the result.
   - Example: x = 2 is equivalent to x = x**2.
"""

